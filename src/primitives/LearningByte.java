package primitives;

public class LearningByte {
    public static void main(String[] args) {
        byte aJsNumber = 18;
        byte gulizarsNumber = 27;
        byte maxByte = 127;
        byte minByte = -128;

        System.out.println(aJsNumber); //18
        System.out.println(gulizarsNumber); //27
        System.out.println(maxByte); // 127 (byte max value)
        System.out.println(minByte); // -128 (byte min value)
    }
}
