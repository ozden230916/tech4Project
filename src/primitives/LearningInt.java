package primitives;

public class LearningInt {
    public static void main(String[] args) {
        // containerType label = data;
        int vitasNumber = 5;
        int miraysNumber = 7;

        int maxInt = 2147483647;
        int minInt = -2147483648;

        System.out.println(vitasNumber); //5
        System.out.println(miraysNumber); //7
        System.out.println(maxInt); //2147483647 (int's max value|)
        System.out.println(minInt); //-2147483648 (int's min value|)

    }
}
