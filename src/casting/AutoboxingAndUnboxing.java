package casting;

public class AutoboxingAndUnboxing {
    public static void main(String[] args) {
        //Autoboxing (primitive to object/ wrapper class)
        float num1 = 45.6F;
        Float num2 = num1;

        //Unboxing (wrapper class/ object to primitive)
        Character character1 = 'D';
        char character2 = character1;


    }
}
