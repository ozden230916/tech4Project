package primitives;

public class LearningShort {
    public static void main(String[] args) {
        short datasNumber = 45;
        short alonasNumber = 13;
        short maxShort = 32767;
        short minShort = -32768;

        System.out.println(datasNumber);// 45
        System.out.println(alonasNumber);// 13
        System.out.println(maxShort);// 32767 ( max value of short)
        System.out.println(minShort);// -32768 (min value of short)
    }
}
